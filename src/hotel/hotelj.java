    public void sale()
    {
        String totalcost = txtcost.getText();
        String pay = txtpayment.getText();
        String balance = txtbalance.getText();
        
        
        
        int lastid = 0;
        
        
        try {
            String query = "insert into sales(subtotal,pay,bal)value(?,?,?)";
            pst = con.prepareStatement(query,Statement.RETURN_GENERATED_KEYS);
            pst.setString(1, totalcost);
            pst.setString(2, pay);
            pst.setString(3, balance);
            pst.executeUpdate();
            
            
            rs =pst.getGeneratedKeys();
            
            
            if(rs.next()){
                
                lastid = rs.getInt(1);
            }
            
            int rows = jTable1.getRowCount();
            
            
            
            String query1 = "insert into sales_item(sales_id,dishname,price,qty,total)value(?,?,?,?,?)";
            pst1 = con.prepareStatement(query1);
            
            String dishname = "";
            String price;
            String qty;
            int total = 0;
            
            for(int i= 0; i<jTable1.getRowCount(); i++)
            {
                dishname = (String)jTable1.getValueAt(i, 0);
                price = (String)jTable1.getValueAt(i, 1);
                qty = (String)jTable1.getValueAt(i, 2);
                total = (int)jTable1.getValueAt(i, 3);
                
                pst1.setInt(1, lastid);
                pst1.setString(2, dishname);
                pst1.setString(3, price);
                pst1.setString(4, qty);
                pst1.setInt(5, total);
                
                pst1.executeUpdate();
                
                
                
            }
            JOptionPane.showMessageDialog(this, "Sales is completed");
            
            
            
            
            
        } catch (SQLException ex) {
            Logger.getLogger(hotelj.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
            
            
            
        
        
        
        
    }
    
    
    
    

